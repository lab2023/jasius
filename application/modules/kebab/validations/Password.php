class Kebab_Validation_Password
{
    /**
     * Validate the password
     *
     * @static
     * @param $password
     * @return bool
     */
    public static function isValid($password)
    {
        $validatorPassword = new Zend_Validate();
        $validatorPassword->addValidator(new Zend_Validate_StringLength(4, 16));
        return $validatorPassword->isValid($password);
    }
}
